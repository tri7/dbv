CREATE TABLE `produtos_hist` (
  `id` int(10) unsigned NOT NULL,
  `titulo` varchar(100) DEFAULT NULL,
  `status_id` tinyint(3) unsigned DEFAULT NULL,
  `tipo_de_produto_id` tinyint(3) unsigned DEFAULT NULL,
  `data_criacao` datetime NOT NULL,
  `data_modificacao` datetime NOT NULL,
  `fornecedor_id` mediumint(11) NOT NULL,
  `versao_id` smallint(2) NOT NULL,
  `comentarios` text,
  `update_number` mediumint(11) unsigned NOT NULL,
  `operacao` char(6) DEFAULT NULL,
  `data_criacao_hist` timestamp NULL DEFAULT CURRENT_TIMESTAMP,
  `data_envio` datetime DEFAULT NULL,
  `mod_fields` varchar(100) NOT NULL DEFAULT '',
  `ws_id` tinyint(3) unsigned NOT NULL,
  `error` varchar(100) DEFAULT NULL,
  `error_date` datetime DEFAULT NULL,
  `statusprod` enum('APAGADO','DONE','OVERRIDE','ACTIVO') NOT NULL DEFAULT 'ACTIVO',
  PRIMARY KEY (`mod_fields`,`update_number`,`id`,`statusprod`),
  KEY `fk_produtos_status_de_produto1_idx` (`status_id`),
  KEY `fk_produtos_tipos_de_produtos1_idx` (`tipo_de_produto_id`),
  KEY `fk_produtos_versoes_gmedia1_idx` (`versao_id`),
  KEY `fk_produtos_parceiros1_idx` (`fornecedor_id`),
  KEY `forn_versao_idx` (`fornecedor_id`,`versao_id`),
  KEY `fk_produtos_hist_servicosweb1` (`ws_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COMMENT='Tabela de histórico. Ou seja, os dados actuais estão na produtos. Tabela utilizada pelos servico updates_clientes'