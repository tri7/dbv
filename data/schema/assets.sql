CREATE TABLE `assets` (
  `id` mediumint(11) NOT NULL AUTO_INCREMENT COMMENT 'status encomendado, nao encomendado, tx ready\n',
  `tipo_de_asset_id` mediumint(11) DEFAULT NULL COMMENT 'video\naudio\nimagem\nlegendas\n',
  `status_id` mediumint(11) DEFAULT NULL,
  `qc_id` mediumint(11) DEFAULT NULL,
  `precedencia` mediumint(11) DEFAULT NULL,
  `part_num` mediumint(2) DEFAULT NULL,
  `data_de_criacao` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `quem_criou` mediumint(11) NOT NULL,
  `ultimo_update` datetime DEFAULT NULL,
  `quem_ultimo_update` mediumint(11) DEFAULT NULL,
  `descricao` varchar(500) DEFAULT NULL,
  `ISAN` varchar(45) DEFAULT NULL,
  `relatorio_tecnico` varchar(45) DEFAULT NULL,
  `num_de_asset` varchar(15) DEFAULT NULL,
  `titulo_historico_original` varchar(256) DEFAULT NULL,
  `qc_ingest_id` mediumint(11) DEFAULT NULL,
  `qc_tx_pilat_id` mediumint(11) DEFAULT NULL,
  `versoes_gmedia_id` smallint(2) DEFAULT NULL,
  `service` tinyint(1) unsigned NOT NULL DEFAULT '0' COMMENT 'Se tiver sido inserido/update por terms service = 1. ',
  PRIMARY KEY (`id`),
  KEY `fk_assets_tipos_de_assets1_idx` (`tipo_de_asset_id`),
  KEY `fk_asset_status_idx` (`status_id`),
  KEY `fk_assets_qcs1_idx` (`qc_id`),
  KEY `fk_assets_assets1_idx` (`precedencia`),
  KEY `fk_assets_parceiros1_idx` (`quem_criou`),
  KEY `fk_assets_quem_ultimo_update1_idx` (`quem_ultimo_update`),
  KEY `qc_ingest_id` (`qc_ingest_id`),
  KEY `qc_tx_pilat_id` (`qc_tx_pilat_id`),
  KEY `id` (`id`),
  KEY `fk_assets_versoes_gmedia1_idx` (`versoes_gmedia_id`),
  CONSTRAINT `assets_ibfk_1` FOREIGN KEY (`qc_ingest_id`) REFERENCES `qcs` (`id`) ON DELETE NO ACTION ON UPDATE NO ACTION,
  CONSTRAINT `assets_ibfk_2` FOREIGN KEY (`qc_tx_pilat_id`) REFERENCES `qcs` (`id`) ON DELETE NO ACTION ON UPDATE NO ACTION,
  CONSTRAINT `fk_assets_assets1` FOREIGN KEY (`precedencia`) REFERENCES `assets` (`id`) ON DELETE NO ACTION ON UPDATE NO ACTION,
  CONSTRAINT `fk_assets_parceiros1` FOREIGN KEY (`quem_criou`) REFERENCES `parceiros` (`parceiro_id`) ON DELETE NO ACTION ON UPDATE NO ACTION,
  CONSTRAINT `fk_assets_parceiros2` FOREIGN KEY (`quem_ultimo_update`) REFERENCES `parceiros` (`parceiro_id`) ON DELETE NO ACTION ON UPDATE NO ACTION,
  CONSTRAINT `fk_assets_qcs1` FOREIGN KEY (`qc_id`) REFERENCES `qcs` (`id`) ON DELETE NO ACTION ON UPDATE NO ACTION,
  CONSTRAINT `fk_assets_tipos_de_assets1` FOREIGN KEY (`tipo_de_asset_id`) REFERENCES `tipos_de_assets` (`id`) ON DELETE NO ACTION ON UPDATE NO ACTION,
  CONSTRAINT `fk_assets_versoes_gmedia1` FOREIGN KEY (`versoes_gmedia_id`) REFERENCES `versoes_gmedia` (`id`) ON DELETE NO ACTION ON UPDATE NO ACTION,
  CONSTRAINT `fk_asset_status` FOREIGN KEY (`status_id`) REFERENCES `assets_status` (`id`) ON DELETE NO ACTION ON UPDATE NO ACTION
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COMMENT='Participa em GMEDIA e Envia para terms. Campos definidos nos triggers'