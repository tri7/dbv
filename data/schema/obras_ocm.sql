CREATE TABLE `obras_ocm` (
  `id` mediumint(11) NOT NULL AUTO_INCREMENT,
  `gcd_id` mediumint(11) DEFAULT NULL,
  `pilat_id` mediumint(11) DEFAULT NULL,
  `outros_id` mediumint(11) DEFAULT NULL,
  `metadados_individual_id` mediumint(11) DEFAULT NULL,
  `tipos_de_obra_id` mediumint(11) NOT NULL,
  `num_episodio` mediumint(3) DEFAULT NULL,
  `num_total_episodios` smallint(6) DEFAULT NULL,
  `status` tinyint(1) DEFAULT NULL,
  `serie_id` mediumint(11) DEFAULT NULL,
  `terms_id` mediumint(11) DEFAULT NULL,
  `ep_airing_order_num` mediumint(3) DEFAULT NULL,
  `deleted_flag` tinyint(1) DEFAULT '0',
  PRIMARY KEY (`id`),
  KEY `fk_obras_pilat1_idx` (`pilat_id`),
  KEY `fk_obras_tipos_de_obra1_idx` (`tipos_de_obra_id`),
  KEY `fk_obras_metadados_obras2_idx` (`metadados_individual_id`),
  KEY `fk_obras_ocm_outros1_idx` (`outros_id`),
  KEY `serie_id` (`serie_id`),
  KEY `metadados_individual_id` (`metadados_individual_id`),
  KEY `terms_id_idx` (`terms_id`),
  CONSTRAINT `fk_obras_metadados_obras2` FOREIGN KEY (`metadados_individual_id`) REFERENCES `metadados_obras` (`id`) ON DELETE NO ACTION ON UPDATE NO ACTION,
  CONSTRAINT `fk_obras_ocm_obras_ocm1` FOREIGN KEY (`serie_id`) REFERENCES `obras_ocm` (`id`) ON DELETE NO ACTION ON UPDATE NO ACTION,
  CONSTRAINT `fk_obras_ocm_outros1` FOREIGN KEY (`outros_id`) REFERENCES `outros` (`id`) ON DELETE NO ACTION ON UPDATE NO ACTION,
  CONSTRAINT `fk_obras_pilat1` FOREIGN KEY (`pilat_id`) REFERENCES `pilat` (`id`) ON DELETE NO ACTION ON UPDATE NO ACTION,
  CONSTRAINT `fk_obras_tipos_de_obra1` FOREIGN KEY (`tipos_de_obra_id`) REFERENCES `tipos_de_obra` (`id`) ON DELETE NO ACTION ON UPDATE NO ACTION
) ENGINE=InnoDB DEFAULT CHARSET=utf8